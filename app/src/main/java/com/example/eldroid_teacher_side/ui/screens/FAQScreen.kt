@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun FAQScreen(navController: NavController) {
    val faqList = listOf(
        FAQItem(
            "How to sync attendance?",
            "Go to the Attendance tab and click the sync icon at the top right."
        ),
        FAQItem(
            "How to contact parents?",
            "Use the Messages tab to find the student's primary guardian."),

        FAQItem(
            "How to edit grades?",
            "Navigate to the Grades tab, select a student, and tap the edit icon.")
    )

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("Frequently Asked Questions") },
                navigationIcon = {
                    IconButton(onClick = { navController.popBackStack() }) {
                        Icon(Icons.Default.ArrowBack, contentDescription = null)
                    }
                }
            )
        }
    ) { padding ->
        LazyColumn(
            modifier = Modifier.padding(padding).padding(16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            items(faqList) { faq ->
                FAQAccordionItem(faq)
            }
        }
    }
}
